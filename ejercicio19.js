// 19. Calculadora básica
let num1 = 12;
let num2 = 3;
let operacion = '+';
if (operacion === '+') {
    console.log(num1 + num2);
} else if (operacion === '-') {
    console.log(num1 - num2);
} else if (operacion === '*') {
    console.log(num1 * num2);
} else if (operacion === '/') {
    console.log(num1 / num2);
} else {
    console.log("Operación no válida");
}
